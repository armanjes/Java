public class RelationalOperator {
    public static void main(String[] args) {
        
        int a, b;
        a = 10; b = 20;

        System.out.print(a==b);
        System.out.print(a!=b);
        System.out.print(a>b);
        System.out.print(a<b);
        System.out.print(a<=b);
        System.out.print(a>=b);
    }
}
